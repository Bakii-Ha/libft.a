void ft_putnbr_fd(int a, int fd)
{
    long n;
    n = a;

    if(n < 0)
    {
        write(fd,"-",1);
        n *= -1;
    }
    if(n > 9)
    {
        ft_putnbr_fd((n /10),fd);
        ft_putchar_fd((n % 10) + 48,fd);

    }

    else 
        ft_putchar_fd(n + 48,fd);
}